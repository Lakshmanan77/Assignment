package week2Day1;

public class OddOrEven {

	public static void main(String[] args) {
		int number = 7;

		// 7/2 = 3 rem=1

		if (number % 2 != 0) {
			System.out.println("Number is odd");
		}

		else {
			System.out.println("Number is even");
		}

		// Number is odd

	}

}
